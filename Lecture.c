#include <stdio.h>

union Price {
    float discount;
    float price;
};

struct Product {
    char name[50];
    char type; // D or R
    union Price p;
};

int main() {
    struct Product pr;

    scanf("%s", pr.name);
    scanf(" %c", &pr.type);

    if (pr.type == 'D') {
        scanf("%f", &pr.p.discount);
    } else {
        scanf("%f", &pr.p.price);
    }

    printf("%s ", pr.name);

    if (pr.type == 'D') {
        printf("Discount: %.2f%%\n", pr.p.discount);
    } else {
        printf("Price: %.2f\n", pr.p.price);
    }

    return 0;
}        